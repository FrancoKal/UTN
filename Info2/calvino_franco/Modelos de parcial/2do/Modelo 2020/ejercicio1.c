#include <stdio.h>
#include <stdint.h>

int main (void)
{
    uint32_t valor, resultado;

    /*
        Hexa:       F       E       D       C       B       A       2       1
        Binario:    1111    1110    1101    1100    1011    1010    0010    0001

        valor (en binario) =    1111    1110    1101    1100    1011    1010    0010    0001
    */    
    valor = 0xFEDCBA21;

    /*
        0xFFFF = 0x0000FFFF = 0000  0000    0000    0000    1111    1111    1111    1111 (en binario)

        valor   =   1111    1110    1101    1100    1011    1010    0010    0001
        &
        0xFFFF  =   0000    0000    0000    0000    1111    1111    1111    1111
        ------------------------------------------------------------------------
                =   0000    0000    0000    0000    1011    1010    0010    0001
        0xBA21  =   0       0       0       0       B       A       2       1
    */
    resultado = valor & 0xFFFF; //0xBA21

    /*
        0xFFFF = 0x0000FFFF = 0000  0000    0000    0000    1111    1111    1111    1111 (en binario)

        valor   =   1111    1110    1101    1100    1011    1010    0010    0001
        |
        0xFFFF  =   0000    0000    0000    0000    1111    1111    1111    1111
        ------------------------------------------------------------------------
                =   1111    1110    1101    1100    1011    1010    0010    0001
                =   F       E       D       C       B       A       2       1
    */
    resultado = valor | 0xFFFF; //0xFEDCBA21

    /*
        valor >> 4  =   0000    1111    1110    1101    1100    1011    1010    0010
                    =   0       F       E       D       C       B       A       2
    */
    resultado = valor >> 4; //0x0FEDCBA2

    /*
        El resultado anterior es 0x0FEDCBA2

        resultado   =   0000    1111    1110    1101    1100    1011    1010    0010
        |
        1 << 2      =   0000    0000    0000    0000    0000    0000    0000    0100
        ----------------------------------------------------------------------------
        resultado   =   0000    1111    1110    1101    1100    1011    1010    0110
                    =   0       F       E       D       C       B       A       6
    */   
    resultado |= (1 << 2);  //0x0FEDCBA6

    /*
        El resultado anterior es 0x0FEDCBA6

        resultado   =   0000    1111    1110    1101    1100    1011    1010    0010
        &
        ~(1 << 2)   =   1111    1111    1111    1111    1111    1111    1111    1011
        ----------------------------------------------------------------------------
        resultado   =   0000    1111    1110    1101    1100    1011    1010    0010
                    =   0       F       E       D       C       B       A       2
    */   
    resultado &= ~(1 << 2); //0x0FEDCBA2

    return 0;
}